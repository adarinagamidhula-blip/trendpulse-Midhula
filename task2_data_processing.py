import pandas as pd
import os
from datetime import datetime

# ---- STEP 1: LOAD JSON FILE ----

# Get today's date in YYYYMMDD format (same format used in Task 1)
today = datetime.now().strftime("%Y%m%d")

# File path
file_path = f"data/trends_{today}.json"

# Check if file exists
if not os.path.exists(file_path):
    print(f"File not found: {file_path}")
    exit()

# Load JSON into DataFrame
df = pd.read_json(file_path)

# Print initial row count
print(f"Loaded {len(df)} stories from {file_path}\n")


# ---- STEP 2: CLEAN THE DATA ----

# 1. Remove duplicates based on post_id
before = len(df)
df = df.drop_duplicates(subset="post_id")
print(f"After removing duplicates: {len(df)}")

# 2. Remove rows with missing critical values
df = df.dropna(subset=["post_id", "title", "score"])
print(f"After removing nulls: {len(df)}")

# 3. Convert data types
# Convert score and num_comments to integers
df["score"] = df["score"].astype(int)
df["num_comments"] = df["num_comments"].fillna(0).astype(int)

# 4. Remove low-quality posts (score < 5)
df = df[df["score"] >= 5]
print(f"After removing low scores: {len(df)}")

# 5. Strip whitespace from title
df["title"] = df["title"].str.strip()


# ---- STEP 3: SAVE CLEAN DATA ----

# Output file path
output_path = "data/trends_clean.csv"

# Save to CSV
df.to_csv(output_path, index=False)

# Confirmation message
print(f"\nSaved {len(df)} rows to {output_path}\n")


