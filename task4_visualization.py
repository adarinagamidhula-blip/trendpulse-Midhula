import pandas as pd
import numpy as np
import os

# ---- STEP 1: LOAD DATA ----

file_path = "data/trends_clean.csv"

# Check if file exists
if not os.path.exists(file_path):
    print(f"File not found: {file_path}")
    exit()

# Load CSV into DataFrame
df = pd.read_csv(file_path)

# Print shape
print(f"Loaded data: {df.shape}\n")

# Print first 5 rows
print("First 5 rows:")
print(df.head(), "\n")

# Average score and comments (using Pandas)
avg_score = df["score"].mean()
avg_comments = df["num_comments"].mean()

print(f"Average score   : {int(avg_score):,}")
print(f"Average comments: {int(avg_comments):,}\n")


# ---- STEP 2: NUMPY ANALYSIS ----

print("--- NumPy Stats ---")

scores = df["score"].values

# NumPy calculations
mean_score = np.mean(scores)
median_score = np.median(scores)
std_score = np.std(scores)
max_score = np.max(scores)
min_score = np.min(scores)

print(f"Mean score   : {int(mean_score):,}")
print(f"Median score : {int(median_score):,}")
print(f"Std deviation: {int(std_score):,}")
print(f"Max score    : {max_score:,}")
print(f"Min score    : {min_score}\n")

# Category with most stories
category_counts = df["category"].value_counts()
top_category = category_counts.idxmax()
top_count = category_counts.max()

print(f"Most stories in: {top_category} ({top_count} stories)\n")

# Story with most comments
most_commented = df.loc[df["num_comments"].idxmax()]

print("Most commented story:")
print(f"\"{most_commented['title']}\" — {most_commented['num_comments']:,} comments\n")


# ---- STEP 3: ADD NEW COLUMNS ----

# Engagement = comments per upvote (avoid division by zero using +1)
df["engagement"] = df["num_comments"] / (df["score"] + 1)

# is_popular = score greater than average
df["is_popular"] = df["score"] > avg_score


# ---- STEP 4: SAVE UPDATED DATA ----

output_path = "data/trends_analysed.csv"

df.to_csv(output_path, index=False)

print(f"Saved to {output_path}")

