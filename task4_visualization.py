# Create outputs folder if it doesn't exist
os.makedirs("outputs", exist_ok=True)


# ---- HELPER FUNCTION ----
# Shorten long titles (max 50 characters)
def shorten_title(title, max_len=50):
    return title if len(title) <= max_len else title[:47] + "..."


# ---- CHART 1: TOP 10 STORIES BY SCORE ----

# Sort and get top 10
top_stories = df.sort_values(by="score", ascending=False).head(10)

# Apply title shortening
top_stories["short_title"] = top_stories["title"].apply(shorten_title)

plt.figure(figsize=(10, 6))
plt.barh(top_stories["short_title"], top_stories["score"], color="skyblue")
plt.xlabel("Score")
plt.ylabel("Story Title")
plt.title("Top 10 Stories by Score")
plt.gca().invert_yaxis()  # Highest score on top

# Save before show
plt.savefig("outputs/chart1_top_stories.png")
plt.close()


