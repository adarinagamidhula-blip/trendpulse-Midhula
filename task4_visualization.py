import pandas as pd
import matplotlib.pyplot as plt
import os

# ---- STEP 1: LOAD DATA & SETUP ----

file_path = "data/trends_analysed.csv"

# Check if file exists
if not os.path.exists(file_path):
    print(f"File not found: {file_path}")
    exit()

